import matplotlib.pyplot as plt

# ----------------------
# 1. 修正数据：按实验路径排列，并补充起点到末尾（实现闭合）
# ----------------------
# 正向励磁（H从负→正） + 反向去磁（H从正→负） + 补充起点（实现闭合）
H = [
    -115.2, -32.0, 12.8, 16.0, 48.0, 67.2, 73.6, 80.0, 96.0, 108.8, 115.2,  # 正向到正饱和
    32.0, -22.4, -64.0, -83.2, -92.8, -105.6,                               # 反向下降
    -115.2  # 补充起点，让回线闭合
]
B = [
    -276, -248, -176, -164, -77.6, -44, 76, 128, 216, 276, 324,             # 正向到正饱和
    248, 180, 52, 48, -116, -212,                                           # 反向下降
    -276    # 补充起点，让回线闭合
]

# ----------------------
# 2. 解决中文乱码
# ----------------------
plt.rcParams['font.sans-serif'] = ['SimHei']
plt.rcParams['axes.unicode_minus'] = False

# ----------------------
# 3. 绘图（和示例图样式一致）
# ----------------------
plt.figure(figsize=(8, 6))

# 绘制带圆点的折线，线宽和点大小和示例匹配
plt.plot(
    H, B,
    marker='o',          # 数据点画圆点
    linestyle='-',       # 实线连接
    color='#1f77b4',     # 蓝色（和示例一致）
    linewidth=2,         # 线条粗细
    markersize=8,        # 圆点大小
    markerfacecolor='#1f77b4',
    markeredgecolor='#1f77b4'
)

# ----------------------
# 4. 图表细节设置
# ----------------------
plt.title("磁滞回线图", fontsize=18, pad=20)
plt.xlabel("H/(A/m)", fontsize=14)
plt.ylabel("B/mT", fontsize=14)

# 调整坐标轴范围，完整显示所有数据（包含补充的起点）
plt.xlim(-130, 130)   # H范围：-130~130（留余量，避免点贴边）
plt.ylim(-350, 350)   # B范围：-350~350（覆盖你的B值范围-276~324）

plt.grid(True, linestyle='--', alpha=0.7)  # 添加网格线
plt.tight_layout()  # 自动调整布局，避免标签被截断

# 显示/保存图片
plt.show()
